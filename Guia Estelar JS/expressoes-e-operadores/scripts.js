/*
    Expressões e Operadores 

    - Expressions
    - Operators
        - binary (precisa de dois valores, Ex.: soma +)
        - unary (ñ precisa de dois valores, Ex.: ++ (incremento) ou -- (decremento))
        - ternary

*/
 let number = 2

// Binary
console.log(number + 5) // 7

// Unary
console.log(++number) // 3

// Ternary
console.log(true ? 'alo' : 'nada') // parecido com um if